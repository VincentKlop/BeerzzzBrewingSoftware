    public function convertEBCtoHex(float $ebc)
    {
        $roundedEbc = round($ebc);

        if($roundedEbc > 300) {
            $roundedEbc = 300;
        }

        if($roundedEbc > 150 && $roundedEbc < 300) {
            $roundedEbc = $roundedEbc - $roundedEbc % 50;
        }

        $rgbArray = $this->getRGBColorArray();
        return $this->convertRGBtoHex($rgbArray[$roundedEbc]);
    }

    public function convertRGBtoHex(array $rgbArray)
    {   // index 0: Red, index 1: Green, index 2: Blue
        return sprintf("#%02x%02x%02x", $rgbArray[0], $rgbArray[1], $rgbArray[2]);
    }
    

